/**
Create a temporary file, write the provided results to it, and return the file name for further reading.

@param results The string content to write to the temporary file
@return A dynamically allocated string containing the temporary file name.
 */
char* write_results(const char* results) {
    char* temp_filename = NULL;
    FILE* temp_file = NULL;
    
    // Create a unique temporary file name
    char template[] = "/tmp/resultsXXXXXX";
    int fd = mkstemp(template);
    if (fd == -1) {
        return NULL; // Failed to create temporary file
    }
    
    temp_filename = strdup(template); // Allocate memory for the filename
    if (!temp_filename) {
        close(fd);
        return NULL; // Memory allocation failed
    }

    // Open the temporary file for writing
    temp_file = fdopen(fd, "w");
    if (!temp_file) {
        free(temp_filename);
        return NULL; // Failed to open file stream
    }

    // Write the results to the temporary file
    if (fputs(results, temp_file) == EOF) {
        fclose(temp_file);
        free(temp_filename);
        return NULL; // Failed to write to file
    }

    // Close the file stream
    fclose(temp_file);

    return temp_filename; // Return the filename
}

// BEGIN ENTRYPOINT
int main(int argc, char *argv[]) {
    if (argc != 2) {
        fprintf(stderr, "Usage: %s <string_to_write>\n", argv[0]);
        return 1;
    }

    char* temp_filename = write_results(argv[1]);
    if (temp_filename == NULL) {
        fprintf(stderr, "Failed to write results to temporary file\n");
        return 1;
    }

    printf("%s", temp_filename);

    // Clean up
    free(temp_filename);

    return 0;
}
